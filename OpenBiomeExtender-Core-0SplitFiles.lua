	MEGA_TAG_LIST[file]["DETAILOBJECTS"] = true
end

--Copy all the files to their respective folders
COPY_LISTS = {
	["DISTANTOBJECTS"] = {},
	["LANDMARKS"] = {},
	["OBJECTS"] = {},
	["DETAILOBJECTS"] = {},
}

for file, _ in pairs(MEGA_TAG_LIST) do
	for tag, _ in pairs(MEGA_TAG_LIST[file]) do
    vanilla_folder = "METADATA/SIMULATION/SOLARSYSTEM/BIOMES/"
		open_be_folder = "OPENBE/OBJECTFILES/VANILLA/"..tag.."/"
    
    new_file_path = string.gsub(file,vanilla_folder,open_be_folder)
    table.insert(COPY_LISTS[tag],{file,new_file_path})
		
	end
end

--Make lists that say which files need a section removed
DELETE_LISTS = {
	["DISTANTOBJECTS"] = {},
	["LANDMARKS"] = {},
	["OBJECTS"] = {},
	["DETAILOBJECTS"] = {},
}

--For every tag a file has, add itself to the delete list of every other tag it has. (If it has more than one tag).
for file, _ in pairs(MEGA_TAG_LIST) do
	for category, _ in pairs(MEGA_TAG_LIST[file]) do
		for other_category, _ in pairs(MEGA_TAG_LIST[file]) do
			if (category ~= other_category) then
				vanilla_folder = "METADATA/SIMULATION/SOLARSYSTEM/BIOMES/"
				open_be_folder = "OPENBE/OBJECTFILES/VANILLA/"..other_category.."/"
				new_file_path = string.gsub(file,vanilla_folder,open_be_folder)
        table.insert(DELETE_LISTS[category],new_file_path)
			end
		end
	end
end

