	MEGA_TAG_LIST[file]["DISTANTOBJECTS"] = true
end

for _, file in pairs(HAS_LANDMARKS) do
	MEGA_TAG_LIST[file]["LANDMARKS"] = true
end

for _, file in pairs(HAS_OBJECTS) do
	 MEGA_TAG_LIST[file]["OBJECTS"] = true
end

