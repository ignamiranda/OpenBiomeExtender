MEGA_TAG_LIST = {}

for _, array in pairs({HAS_DISTANTOBJECTS,HAS_LANDMARKS,HAS_OBJECTS,HAS_DETAILOBJECTS}) do
	for _, file in pairs(array) do
		MEGA_TAG_LIST[file] = {}
	end
end

